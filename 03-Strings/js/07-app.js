/*************************** 
STRINGS METHODS - CONVERTIR A MAYUSCULAS O MINUSCULAS
***************************/

const producto = "Monitor de 20 Pulgadas";

/* 
  Transforma el texto a MAYUSCULAS
*/
console.log(producto.toUpperCase()); // MONITOR DE 20 PULGADAS

/* 
  Transforma el texto a minusculas
*/
console.log(producto.toLowerCase()); // monitor de 20 pulgadas

/* 
  Transforma un valor numerico a cadena de texto
*/
const precio = 300;
console.log(precio.toString()); // "300"
