/*************************** 
OPERACIONES EN JAVASCRIPT
***************************/

const numero1 = 20;
const numero2 = 30;

let resultado;

/* 
  Suma
*/
resultado = numero1 + numero2; // 50

/* 
  Resta
*/
resultado = numero1 - numero2; // 10

/* 
  Multiplicar
*/
resultado = numero1 * numero2; // 600

/* 
  Division
*/
resultado = numero1 / numero2; // 0.666666

/* 
  Modulo
*/
resultado = numero1 % numero2; // 20

console.log(resultado);
