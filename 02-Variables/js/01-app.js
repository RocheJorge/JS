/*************************** 
VARIABLES CON VAR
***************************/

/*  
  Inicializamos una variable con un valor
*/
var producto = "Monitor de 24 pulgadas";

/*  
  Las variables se pueden reasignar
*/
producto = "Monitor de 19 pulgadas";
console.log(producto); // Monitor de 19 pulgadas

/*  
  JavaScript es un lenguaje de tipo dinamico, no se especifica el tipo de dato
*/
producto = 20;
console.log(producto); // 20

/*  
  Se pueden inicializar sin valor y asignarlo despues
*/
var disponible;
disponible = true;
disponible = false;

/*  
  Inicializar multiples variables
*/
var categoria = "Computadoras";
marca = "Marca Famosa";
calificacion = 5;
